'''3. Faça um Programa que peça a quantidade de quilômetros, transforme
em metros, centímetros e milímetros.'''

km = int(input('Digite a quantidade de quilômetros (km): '))

metro = km * 1000
centimetro = km * 100000
milimetro = km * 1000000

print('Metros: ', metro)
print('Centimetros: ', centimetro)
print('Milimetro: ', milimetro)